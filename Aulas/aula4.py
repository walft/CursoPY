# int -> Número inteiro
# O tipo int representa qualquer número positivo ou negativo. int sem sinal é considerado positivo.

# float -> Número com ponto flutuante.
# O tipo float representa qualquer número possitivo ou negativo com ponto flutuante.
# float sem sinal é considerado possitivo.

# A função type mostra o tipo que o Python inseriu ao valor.

