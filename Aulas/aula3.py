# Escape \ sempre o próximo caracter será inteprestado

print('Luis Henrque Gonçalves \'psicólogo\'')

# r na frente da string faz com que o caractere de escape seja exibido
print(r'Luis Henrque Gonçalves \'psicólogo\'')

